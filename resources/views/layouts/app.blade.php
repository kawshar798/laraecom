                    url: "{{url('add/cart')}}/"+id,
                    type:"GET",
                    dataType:'json',
                    success:function (result) {
                        if (result.success == true) {
                            toastr.success(result.messege);

                        } else {
                            toastr.error(result.messege);
                        }
                    }

                })
            }
        });
    })
